            Eligibility

            Applicants for grants must satisfy the following conditions of eligibility: 
            
            Condition for Eligibility
            
            Be a registered Not-For-Profit organisation working in Nigeria (registration could be with the Corporate Affairs Commission, State Agency or other relevant bodies);
            2. Have a verifiable organisational structure;
            3. Proposals that promote the overall objectives of the Foundation;
            4. Demonstrate community participation and government involvement;
            5. Have a track record of engagement in similar or related activities;
            6. Possess a valid financial audit report (maximum two (2) years).
            Ineligible Applicants
            
            TYDF grants WILL NOT be extended to the following:
            Individuals;
            Organisations that are not legally registered in Nigeria;
            A government entity;
            Any organisation that has mismanaged TYDF funds in the past;
            Political associations and institutions, or their subsidiaries and affiliates;
            Organisations that advocate, promote, or espouse inhumane policies, illegal activities or terrorism;
            Faith-based organisations whose proposed projects are of a religious nature.
            Ineligible Activities and Excluded Costs
            
             Grant funds cannot be utilized for the following causes:
            
            Political campaigns
            Religious or sectarian activities
            Activities to be implemented outside Nigeria
            Overseas training of any nature
            Any activity that is illegal
             
